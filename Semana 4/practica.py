#Escribe un programa en Python que solicite al usuario ingresar la cantidad de emisiones de CO₂ de un vehículo (en gramos por kilómetro) y determine su categoría ambiental según la siguiente escala:  
#- Menos de 50 g/km → `"Vehículo de emisiones ultrabajas (ECO)"`  
#- Entre 50 y 100 g/km → `"Vehículo de bajas emisiones"`  
#- Entre 101 y 150 g/km → `"Vehículo con emisiones moderadas"`  
#- Entre 151 y 200 g/km → `"Vehículo de altas emisiones"`  
#- Más de 200 g/km → `"Vehículo altamente contaminante"`  
#- Si el valor ingresado es negativo, mostrar `"Valor no válido."`  

#El programa también debe determinar si el vehículo paga o no impuesto ambiental, según la siguiente regla:  
#- Si las emisiones son mayores a 120 g/km, el vehículo debe pagar un impuesto ambiental del 10% sobre su precio base.  
#- Si las emisiones son 120 g/km o menos, el vehículo está exento de impuesto ambiental.  

#El usuario deberá ingresar el precio base del vehículo y el programa calculará, en caso de aplicar, el monto del impuesto.


